    <div class="content">
        <p>Nobles Combattants à cheval, grands justiciers à l’épée, les chevaliers, appelés aussi cavaliers sont un
            classique et un incontournable déguisement pour toute les fêtes de vos enfants. Que ce soit pour un
            spectacle d’école où d’autre fêtes à thème médiéval ou renaissance, ce costume enfant chevalier sera parfait
            pour votre petit en plus de couter très peu pour votre portefeuille.

        <h2>Pour quelle occasion ?</h2>

        <p>Ce costume enfant chevalier peut être enfiler pour toutes les fêtes d’anniversaires à thèmes ainsi que
            d’autres fêtes médiévales comme “la fête du roi l’oiseau” au Puy-en-Velay ou encore pour les marchés
            médiévaux tel que ‘’De Charlemagne aux chevaliers’’. Sa tenue de cavalier lui permettra de s’intégrer à
            l’ambiance moyen âge de ces fêtes à merveille et ne manquera pas d’attirer l’attention des jolies princesses
            aux alentours.

        <h2>La qualité de nos déguisements</h2>

        <p>Nos costumes d’enfants chevaliers sont confectionnés seulement avec de matériaux de très bonne qualitée par
            de grands artisans travaillant le cuir avec une grande dextérité. Ces grands artisans sont reconnus au
            Puy-en-Velay. Leurs créations sont mises en avant dans de grandes fêtes médiévales tel que le roi l’oiseau.
            Solides et flexibles, vos enfants se sentiront très à l’aise dans leurs costumes d’enfant chevalier, le
            tissu étant très doux et confortable. Vous n’avez rien à craindre en ce qui concerne la durabilité de ces
            déguisements d’autre temps car ils sont choisis avec grand soin par nos artisans du Puy.

        <h2>Quels sont les pièces qui composent le costume</h2>

        <p>Ce costume d’enfant chevalier se compose d’un haut en tissu, d’un pantalon en coton, d’une cape en satin, de
            couvre-bottes et ceinture en cuir. Juste de quoi avoir l’air d’un vrai petit chevalier de la table ronde !

        <h2>Idée d’accessoire pour le costume médiéval</h2>

        <p>Pour compléter à merveille ce costume d’enfant chevalier et d’avoir l’air d’un vrai guerrier, vous pouvez
            vous offrir les accessoires suivants:
        <ul>
            <li>Casque en faux métal dorée</li>
            <li>Armure en faux métal dorée</li>
            <li>Épée en bois médiévale bleue</li>
            <li>Bouclier en bois de l’ordre des chevaliers rouge</li>
            <li>Fausse fourrure noire du roi l’oiseau</li>
        </ul>

        <p>Avec cela, vous pourrez ajouter une petite touche renaissance à votre accoutrement et épater toutes les
            princesses à la taverne.


        <h2>Tailles</h2>

        <p>Pour les tailles de ce déguisement, nous recommandons les âges de 3 ans jusqu’à 11 ans.
            Cependant pour accompagner votre enfant et de partager de bons moments avec lui, nous avons des versions
            pour tout âges et sexes que ce soit pour des femmes, des hommes, des filles ou des garçons. Costumez vos
            enfants pour la fête et vous aurez un vrai bataillon d’enfants chevaliers !

